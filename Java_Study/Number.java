
public class Number {

	public static void main(String[] args) {
		// 오퍼레이터 (operator) 연산자 
		System.out.println(6 + 2); // 8
		System.out.println(6 - 2); // 4
		System.out.println(6*2); // 12
		System.out.println(6/2); // 3
		
		System.out.println(Math.PI); // (Math.PI) = 3.14(원주율,파이를 뜻함)
		System.out.println(Math.floor(Math.PI)); // (Math.floor) = 소수점이 있을시, 소수점을 없애라는 명령어
		System.out.println(Math.floor(Math.ceil(Math.PI))); // (Math.ceil) = 소수점이 있을시 반올림 하라는 명령어
		System.out.println(Math.floor(154.23));
		System.out.println(Math.floor(Math.ceil(154.23)));
	}

}
