
public class StringApp {

	public static void main(String[] args) {
		System.out.println("Hello World"); // String(문자열) 문자 여러개
		System.out.println('H'); // Character (문자) 문자 1개만
		System.out.println("Hello"
				+ "World"); // 문자열+문자열 = 한줄로 문자열끼리 붙여짐
		System.out.println("Hello \nWorld"); // \n = new line (한줄 띄어쓰기 [컨트롤+엔터와 같은역할]
		System.out.println("Hello \"World\""); // escape = '한개'의문자열에 내가 원하는 문자열을 '따로' 추가하고 싶을때 \를 넣어 추가 시켜줌
		
		

	}

}
