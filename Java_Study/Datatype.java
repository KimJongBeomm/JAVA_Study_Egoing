public class Datatype{
	public static void main(String[] args) {
		System.out.println(6); // Number = 숫자
		System.out.println("Six"); // String = 문자열
		
		System.out.println("6"); // String 6 = 숫자가 아닌 문자열 6 으로 인식
		
		System.out.println(6+6); // 숫자+숫자 = 12
		System.out.println("6"+"6"); // 문자열+문자열 = 66
		System.out.println(6*6); // 숫자*숫자 = 36
		// System.out.println("6"*"6"); // 문자열(더하기를 제외한 연산은)문자열 = 인식불가 (에러)
		
		
		
	}
}