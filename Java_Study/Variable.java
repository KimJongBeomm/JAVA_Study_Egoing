// 변수의정의
public class Variable {

	public static void main(String[] args) {

		int a = 1; // Number -> integer(int)  // -2,-1,0,1,2 (정수를 나타냄)
		System.out.println(a);
		
		double b = 1.1; // real number -> double // -2.1,-1.0,0,1.4,2.5 (실수를 나타내며 0을 포함하여 소수점이 있음.)
		System.out.println(b);
		
		String c = "Hello World"; // String (문자열을 나타냄)
		System.out.println(c);
		

	}

}
